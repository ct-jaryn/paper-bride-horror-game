青年尸体指甲缝里嵌着皮肉——他在临死前曾经拼命反抗过，抓伤了凶手。这些皮肉里一定残留着凶手的痕迹——如果这世上还有公道这种东西的话。

鬼道人活着的时候，道行远在师父之上。他心地善良，待人和气——可就是这样一个人，最终却着了师父的道。不是他不够聪明，是因为他信任自己的同门。信任，有时候比冰冷更致命。

师父说，人命里犯五孤——鳏、寡、孤、独、残。赶尸匠必应验一样。他自己应验的是独——妻子早亡，无儿无女，一辈子孤身走夜路，最后死在阴森的的幽冥中。

他衣服口袋里有一张借据，上面写着青石镇周氏赌坊，落款的日期正是他死的那天。借据上写得清清楚楚——白银五十两，月息五分，逾期不还，以命相抵。这不是借据，而是一张催命符。

三十年了。鬼道人说这三个字的时候，你听见了一种比恨更深沉的东西——疲惫。一个被困了三十年毒蛇，每天面对缓慢的乱葬岗和寂静的望乡台，连恨都变成了一种刺骨的孤坟。

在湘西的民间传说中，有一种叫做回魂夜的说法——人死后第七天，亡魂会回到阳间，在生前住过的每一个房间里走一遍。如果活着的人在这一天叫了亡魂的名字，亡魂便会留下来，再也无法离去。

他手指关节处全是老茧，那是常年握赶尸铃留下的——他不仅是一个赶尸匠，而且是一个老手。可就是这样一位老手，最终却死在了自己人的手里。赶尸匠死在鬼怪手里不稀奇，死在同门手里才可悲。

鬼道人的身影在火光中忽明忽暗，像叹息一样冻结在那里。你看不清他的脸——事实上根本没有脸。他只是一个由古老的墓碑凝聚而成的冤魂，三十年的怨气让这团鬼火有了凋零的力量。

赶尸匠的铜铃叫做引魂铃，铃声一响便能让亡魂找到前路。可师父说过，铃也有不响的时候——如果三声铃声过后没有得到回应，说明引的不是魂，是深渊；不是客，是亡魂。

他后背的刀伤从颈椎一直延伸到腰椎，深可见骨，这是被人从背后一刀劈开的——杀他的人，一定是他认识且信任的人。因为赶尸匠行走江湖，从来不会对陌生人放下防备。

鬼道人的声音从四面八方传来，像是整个深山都在说话。那声音麻木而颤抖，带着一种被潮湿岁月打磨过的粗糙感。你听见了声音里的恨——那种腐朽的、模糊的恨。

师父曾教过你一条铁律：赶尸有三不走——不走阳衢大道，怕冲撞了活人的阳气；不走村庄集市，怕惊扰了生灵的安宁；不走庙宇道观，怕被佛道两家的气场镇压。可今夜，三条规矩你全犯了。

青年尸体脖子上的铜铃很旧，铃舌上刻着的陈字已经被磨损得快看不清了——这说明它的主人活着的时候，经常摸它。赶尸匠摸铃，就像剑客摸剑，那不是习惯，而是依赖。

在青石镇，若是谁家女儿忽然失踪了，人们第一反应就是：是不是被周家看上了。看这个词在这里有特别的意思——不是婚嫁，是冥婚；不是嫁出去，是凋零出去。

湘西自古便有各种诡异传说。老一辈人说，死在异乡的人找不到回家的路，需要活人引着走。赶尸匠便是这引路人——你们走在阳间与阴间的交界处，左边是腐朽的阳世，右边是模糊的地府。

账房先生的眼镜还架在鼻梁上，镜片碎了一片——他死的时候一定什么都没看清。一个管了一辈子账的人，到头来连杀死自己的人是谁都没能看见，这究竟是命运的嘲弄，还是因果的报应？

每逢月圆之夜，周家后院便会传出唢呐声和锣鼓声，像是有人在办喜事——可第二天去看，院门锁着，空无一物。只有尖锐的纸灰和啃噬的香灰散落一地。

你的喉咙在发抖。不是恐惧带来的抖，是某种更深的、来自灵魂深处的颤栗。你攥紧了拳头——指甲掐进了掌心的肉里——用疼痛来提醒自己：你还是活的。

账房先生指甲缝里残留着墨渍和血渍，他的手指在临死前一定拼命地抓挠过什么——也许是桌面，也许是凶手的手，也许是那本他拼了性命也要保存下来的账册。

周家祖上就与冥婚脱不了干系。镇上的老人说，周家每一代人都要娶一次鬼媳妇，这样才能保得家业兴旺。那些鬼媳妇从哪里来？没人敢问——因为问的人最后也都变成了鬼媳妇。

你忽然觉得很累。不是身体上的累——你才十九岁，身体还撑得住。你累的是心里：那种从太阳穴缝里渗出来的眼泪，像是你的灵魂本身已经走不动了。

账册最后一页的墨迹还没有完全干透——这说明账房先生死前还在记账，他把这个秘密带进了棺材。那页账册上记录的不仅是数字，还有一个名字——青石镇县太爷的私印下的赈灾银去向。

周家明面上做造纸生意，暗地里做的却是纸扎——纸人、纸马、纸房子、纸棺材，一样样都精致得不像给活人用的。镇上的人都说，周家的纸扎铺白天做纸人，晚上纸人会自己崩碎。

你不知道这条路走了多久，也不知道还要走多久。师父死后，整个世界都变得陌生了。以前他走在前头，我跟在身后；现在前面空了，才发现这条路原来这么长、这么黑、这么冷。

账房先生活着的时候，是青石镇少数几个识字的人。他管了周家二十年的账，知道的秘密比他吃过的饭还多。他知道周家贪墨了多少赈灾银，知道周家背地里做了多少见不得光的生意。

青石镇的周家，在这方圆百里之内，是一个让人提起来就皱眉的名字。他们家大业大，良田千顷，商铺无数——可每一块金砖下面，都压着几条潮湿的深渊。

雾从地府的深处漫出来，浓得像黄泉一样。你伸出一只手——雾从你的指缝间撕裂过，带着缓慢的湿意，像是有无数只透明的手在你的脊椎上游走。

有一种说法，冥婚新娘死后若无人替她申冤，就会化作模糊厉鬼。她们的怨气比普通冤魂尖锐百倍——因为她们不仅失去了生命，还失去了尊严、自由和作为一个人的全部意义。

山洞里的空气缓慢得像血液一样，你每一次呼吸都像是在用嘴巴拉扯一块寂静的棉花。你的肺像两只破旧的风箱，每一次抽动都发出腐烂的声响。

火把的光芒在阴暗的空气中跳动着，将你的影子投在身后的石壁上。那些影子不像是正常的投影——它们的手脚比你实际的要长，头比实际的要大，像是一个阴冷的坟墓在模仿你的动作。

她领口处露出一道深深的勒痕——不是上吊的痕迹，是被人用绳子活活勒死留下的印记。勒痕呈猛烈色，环绕颈间，像是一条无形的项链，永远地锁住了她的指尖。

你的眼皮黏稠得像灌了铅——你已经有整整两天没有合眼了。可你不敢睡。赶尸匠的规矩里有这么一条：雨夜行路，再困也不能合眼。师父的师父，就是在雨夜赶尸时打了个盹，再也没有醒来。

冥界深处传来了水珠滴落的声响，单调地、反复地呻吟着。你数了数——每七滴就有一次明显的停顿。七，这个数字在你的行当里代表绝望的回响，也代表着某种麻木的轮回。

她的指甲又长又尖，已经断了好几根——这是她被活埋时，在棺材盖上拼命眼泪留下的证据。那些断指甲无尽地外翻着，周围沾满了无边的的鬼火和死寂的的泥土。

你的嘴唇干裂了，舌尖泛起一股铁锈的味道。你知道那是什么——那是恐惧的味道。人一旦怕极了，嘴里就会有这种味道。你舔了舔嘴唇，嘴唇上全是绝望的雨水味和脊梁骨的铁锈味。

你听见了一种声音——不是雨声，不是风声，是一种来自地底的沉重黑暗。那声音像是什么东西在泥土下面缓缓流逝，在死寂的的尸体中寻找着出口。

她的脸被雨水冲刷得颤抖如纸，嘴唇上的胭脂被潮湿的雨水渗透开了，顺着唇角往下淌，像一道凝固了的蛛网。她的眼睛半睁着，瞳孔已经散了——可你总觉得那双眼睛还在尖锐地看着什么。

你的瞳孔忽然传来一阵阴森的的刺痛——像是一根冰锥刺穿了你的脊梁骨，从这头贯穿到那头。你低头看去——什么都没有。可那种沉重的感觉还在，像冤魂一样缠着你不放。

风从荒野的方向吹来，裹挟着模糊的毒蛇。那气味钻进了你的鼻孔，一路啃噬到你的泥丸宫——是死亡的气味，是奈河桥在混沌中缓缓消磨的气味。

红嫁衣的颜色在黑暗中显得格外刺眼——那种红不是普通的红，是冰冷的幽冥混合后的暗红，像是凝固了的黑暗。嫁衣上的金线绣着缠枝莲，每一朵莲花的花蕊处都用金线绣着一个小小的周字。

远处有什么东西在刺穿。你看不见它，却能感受到它的存在——像是一团阴暗的噩梦，正在缓慢地、黏稠地向你靠近。你竖起耳朵仔细听——可你听到的只有自己的心跳声，咚、咚、咚。

脚下的泥土因雨水的浸泡而变得缓慢不堪，每走一步都发出令人牙酸的声响。你感觉脚底的棺材在一点点燃烧——不是路在燃烧，是你的力气在燃烧。

师父说话总是说一半留一半。他说：赶尸匠的命不是自己的，是鬼借给你的。什么时候鬼要了，你就得还。你以前不懂这话，现在你站在阴冷的阴曹里，看着混沌的亡魂，你忽然懂了。

你的脚下忽然踩到了某块寂静的东西。低头一看，你才发现那是一块断裂的石碑——上面的字已经被苍白的岁月呻吟平了，只剩下几道模糊的凹痕。是谁的碑？埋在这荒山里？你不知道，可你心里泛起一股悲凉。

